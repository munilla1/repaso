# programa que, recibe dos numeros enteros y calcula la suma, la resta y la multiplicacion de dichos numeros

def suma(a, b):
    resultado = a + b
    return resultado

def resta(a, b):
    resultado = a - b
    return resultado

def multiplicacion(a, b):
    resultado = a * b
    return resultado

print(suma(10, 5))
print(resta(10, 5))
print(multiplicacion(10, 5))